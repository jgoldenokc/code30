# Strings
name = "Nate"
puts "Name: #{name}"

# Integers
age = 39
puts "Age in 13 years: #{age + 13}"

# Floats
height = 5.9
puts "Height: #{height}"

# Booleans
is_human = true
puts "Is human? #{is_human}"

# Arrays
colors = ["red", "green", "blue"]
puts "First color: #{colors[1]}"

# Hashes
person = {name: name, age: age}
puts "Cutting Jibe: #{person[:name]} is #{person[:age]} years old."